export async function generateSystemDocument(
  transactionId: string,
  dto: GenerateDocDto,
  generatedById: string,
): Promise<{ id: string; storageKey: string }> {
  let buffer: Buffer;
  let docTypeKey: 'COMMERCIAL_INVOICE' | 'AML_SCREENING_REPORT' | 'SETTLEMENT_STATEMENT';

  if (dto.documentType === 'COMMERCIAL_INVOICE') {
    buffer = await generateCommercialInvoice(transactionId);
    docTypeKey = 'COMMERCIAL_INVOICE';
  } else if (dto.documentType === 'AML_COC_DECLARATION') {
    buffer = await generateAmlCocDeclaration(transactionId);
    docTypeKey = 'AML_SCREENING_REPORT';
  } else {
    buffer = await generateFinalSettlementStatement(transactionId);
    docTypeKey = 'SETTLEMENT_STATEMENT';
  }

  const timestamp = new Date().toISOString().replace(/[:.]/g, '-');
  const filename = `${dto.documentType}-${timestamp}.pdf`;
  const key = `documents/${transactionId}/${docTypeKey}/${filename}`;

  const { storageKey } = await uploadToS3(key, buffer, 'application/pdf');

  const retainUntil = new Date();
  retainUntil.setFullYear(retainUntil.getFullYear() + 10);

  // Supersede previous system-generated docs of same type
  await prisma.document.updateMany({
    where: {
      transactionId,
      documentType: docTypeKey,
      isSystemGenerated: true,
      approvalStatus: { not: 'SUPERSEDED' },
    },
    data: { approvalStatus: 'SUPERSEDED' },
  });

  const doc = await prisma.document.create({
    data: {
      transactionId,
      documentType: docTypeKey,
      filename,
      storageKey,
      mimeType: 'application/pdf',
      sizeBytes: buffer.length,
      uploadedBy: generatedById,
      isSystemGenerated: true,
      approvalStatus: 'APPROVED',
      approvedBy: generatedById,
      approvedAt: new Date(),
      retainUntil,
    },
  });

  return { id: doc.id, storageKey: doc.storageKey };
}
