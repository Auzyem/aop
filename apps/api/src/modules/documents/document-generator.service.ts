import { PDFDocument, StandardFonts, rgb } from 'pdf-lib';
import { prisma } from '@aop/db';
import { NotFoundError, ValidationError } from '@aop/utils';
import { uploadToS3 } from '../../lib/s3.js';
import type { GenerateDocDto } from './documents.schemas.js';

// ---------------------------------------------------------------------------
// Minimal PDF builder helpers
// ---------------------------------------------------------------------------

async function buildPdfBase(title: string): Promise<{
  pdfDoc: PDFDocument;
  page: ReturnType<PDFDocument['addPage']>;
  font: Awaited<ReturnType<PDFDocument['embedFont']>>;
  boldFont: Awaited<ReturnType<PDFDocument['embedFont']>>;
  y: () => number;
  nextLine: (lines?: number) => void;
}> {
  const pdfDoc = await PDFDocument.create();
  const page = pdfDoc.addPage([595, 842]); // A4
  const font = await pdfDoc.embedFont(StandardFonts.Helvetica);
  const boldFont = await pdfDoc.embedFont(StandardFonts.HelveticaBold);

  let yPos = 800;

  // Header
  page.drawText('AURUM OPERATIONS PLATFORM', {
    x: 50,
    y: yPos,
    size: 8,
    font,
    color: rgb(0.5, 0.5, 0.5),
  });
  yPos -= 20;
  page.drawText(title, {
    x: 50,
    y: yPos,
    size: 16,
    font: boldFont,
    color: rgb(0.1, 0.1, 0.1),
  });
  yPos -= 10;
  page.drawLine({
    start: { x: 50, y: yPos },
    end: { x: 545, y: yPos },
    thickness: 1,
    color: rgb(0.8, 0.8, 0.8),
  });
  yPos -= 20;

  return {
    pdfDoc,
    page,
    font,
    boldFont,
    y: () => yPos,
    nextLine: (lines = 1) => {
      yPos -= lines * 14;
    },
  };
}

function drawField(
  page: ReturnType<PDFDocument['addPage']>,
  label: string,
  value: string,
  x: number,
  y: number,
  font: Awaited<ReturnType<PDFDocument['embedFont']>>,
  boldFont: Awaited<ReturnType<PDFDocument['embedFont']>>,
): void {
  page.drawText(`${label}:`, { x, y, size: 9, font: boldFont, color: rgb(0.3, 0.3, 0.3) });
  page.drawText(value, { x: x + 160, y, size: 9, font, color: rgb(0.1, 0.1, 0.1) });
}

